export function growthRate(status:any) {
  const prev = status.previous_month || 1
  const rate = ((status.current_month - prev) / prev * 100)
  return Math.abs(rate).toFixed(2)
}

export function growthDirection(status:any) {
  return status.current_month >= status.previous_month ? 'up' : 'down'
}

export function growthColor(status:any) {
  return growthDirection(status) === 'up' ? 'success' : 'danger'
}
